package unidad1;

public class OperadoresComparacion {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        System.out.println("¿El numero 1 es igual al numero 2? " + (num1 == num2));
        System.out.println("¿El numero 1 es diferente de numero 2? " + (num1 != num2));
        System.out.println("¿El numero 1 es mayor que numero 2? " + (num1 > num2));
        System.out.println("¿El numero 1 es menor que numero 2? " + (num1 < num2));
        System.out.println("¿El numero 1 es mayor o igual que numero 2? " + (num1 >= num2));
        System.out.println("¿El numero 1 es menor o igual que numero 2? " + (num1 <= num2));
    }
}
